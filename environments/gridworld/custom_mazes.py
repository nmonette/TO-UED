def _make_maze_sampler():
    mazes = jnp.stack(list(STATIC_MAZE_DESIGNS.values()))
    def _sample_maze(rng):
        return mazes[jax.random.randint(rng, (), 0, len(mazes))]
    return _sample_maze

MAZE_DESIGNS = {
    **STATIC_MAZE_DESIGNS,
    'mazes': _make_maze_sampler(),
}
